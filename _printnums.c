#include <stdio.h>
/**
 * main - Entry point of the program
 * 
 * Return: Always 0 (Succsess)
 */

int main(void) 
{
    
    int num1 =24;
    int num2 =-17;
    
    printf("Using %%d specifier :\n");
    printf("num1 = %d\n", num1);
    printf("num2 = %d\n", num2);
    
    printf("\nUsing %%i specifier :\n");
    printf("num1 = %i\n", num1);
    printf("num2 = %i\n", num2);
    
    return 0;
}
